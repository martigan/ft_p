int		ft_strcmp(const char *s1, const char *s2)
{
	char	c1;
	char	c2;

	c1 = 48;
	c2 = 48;
	while (c1 == c2 && (*s1 != '\0' || *s2 != '\0'))
	{
		c1 = *(s1++);
		c2 = *(s2++);
	}
	return (c1 - c2);
}
